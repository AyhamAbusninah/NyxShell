#include "libft.h"

void	ft_lstclear(t_node **lst, void (*del)(void *))
{
	t_node	*tmp;

	if (!lst || !del || !*lst)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = tmp;
	}
}
